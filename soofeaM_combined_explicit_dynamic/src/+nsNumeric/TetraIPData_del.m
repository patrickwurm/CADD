classdef TetraIPData
    
    properties
    end
    
    methods(Static)
        function ip_data = getIpData(number_of_int_points)
            a = (5 + 3*sqrt(5))/20;
            b = (5 - sqrt(5))/20;
            c = (1 + sqrt(5/14))/4;
            d = (1 - sqrt(5/14))/4;
            if number_of_int_points == 1
                ip_data = {{[1/4,1/4,1/4],1.6}};
            elseif number_of_int_points == 4
                ip_data = {{[a,b,b],1/24},...
                      {[b,a,b],1/24},...
                      {[b,b,a],1/24},...
                      {[b,b,b],1/24}};
            elseif number_of_int_points == 5
                ip_data = {{[1/4,1/4,1/4],-4/30},...
                      {[1/2,1/6,1/6],9/120},...
                      {[1/6,1/2,1/6],9/120},...
                      {[1/6,1/6,1/2],9/120},...
                      {[1/6,1/6,1/6],9/120}};
            elseif number_of_int_points == 11
                ip_data = {{[1/4,1/4,1/4],-74/5625},...
                       {[11/14,1/14,1/14],343/45000},...
                       {[1/14,11/14,1/14],343/45000},...
                       {[1/14,1/14,11/14],343/45000},...
                       {[1/14,1/14,1/14],343/45000},...
                       {[c,c,d],56/2250},...
                       {[c,d,c],56/2250},...
                       {[c,d,d],56/2250},...
                       {[d,c,c],56/2250},...
                       {[d,c,d],56/2250},...
                       {[d,d,c],56/2250}};
            end
        end
    end
    
end

